/**
 * STAFF_WORK_CATALOG — the reconciled 66-item reward catalog from the
 * "स्टाफ प्रबंधन" (Staff Management) assessment sheet.
 *
 * Each row's `distribution` encodes the sheet's REMARKS column so awarded point
 * totals stay honest:
 *   SPLIT    — "जितने आदमियों द्वारा किया जायेगा उसमें बट जायेगा" (divided among the doers)
 *   EACH     — "सबको मिलेगा" (every doer gets the full points)
 *   PER_UNIT — "प्रत्येक … पर" / "एक गाड़ी" / "₹1000" / "2 POINTS FOR EACH TANK" (points × quantity)
 *   FLAT     — a fixed one-off award (the default)
 *
 * Points are DERIVED, not hand-set. A `labour` work's `points` shown here is the
 * value produced by `deriveBasePoints()` from its four factors (`timeMin`, plus
 * `skill`/`effort`/`responsibility` on a 0–100 scale) — a generated snapshot. The
 * **factors remain the source of truth**: the seeder recomputes `points` from them
 * on every seed (via `resolveBasePoints()`), so to change a labour work's points you
 * edit its factors (and regenerate this snapshot), never the `points` number directly.
 * The 5 `incentive` works (per-₹1000 fuel sales, customer add/acquisition, Servo-mobil
 * per item) are priced by business policy and keep a typed `points`; the formula does
 * not apply to them (see `INCENTIVE_WORK_CODES`).
 *
 * `requiresApproval` mirrors the sheet's "डीलर अथवा मैनेजर की स्वीकृति आवश्यक" flag
 * (informational — the owner/manager is the one awarding). `titleHi` is kept
 * verbatim for traceability; dealers only ever see the clean `label*` fields.
 */

